package com.mavenproject1;

public class Example1 {

	public static void main(String[] args) {
		// Arithmetic operations
		int a= 40, b= 20 , sum , sub, mul, div, mod;   //declare variables
		sum = a+b;                                     // addition operators
		sub = a-b;                                      // subtraction operators
		mul = sum*sub;                                  // multiplication operators
		div = mul%sub;                                  // division operators
		mod = mul/sub;                                   // modulo operators
		System.out.println("sum of two numbers are:"+sum);
		System.out.println("subtract of two numbers are:"+sub);
		System.out.println("Multiple of these two numbers are:"+mul);
		System.out.println("Division of these two numbers are:"+div);
		System.out.println("modulo are:"+mod);

	}

	
}
